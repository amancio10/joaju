export function translate(key: string, lang: string = "es", replacements: Record<string, string | number> = {}) {
  const dict = translations[lang as keyof typeof translations] || translations.es;
  let text = dict[key as keyof typeof dict] || translations.es[key as keyof typeof translations.es] || key;
  for (const [k, v] of Object.entries(replacements)) {
    text = text.replace(new RegExp(`{${k}}`, "g"), String(v));
  }
  return text;
}

export function useL10n(lang: string = "es") {
  const prefix = lang === "es" ? "" : `/${lang}`;
  return {
    t: (key: string, replacements: Record<string, string | number> = {}) => translate(key, lang, replacements),
    link: (path: string) => {
      if (path.startsWith("http") || path.startsWith("mailto:") || path.startsWith("tel:")) {
        return path;
      }
      const cleanPath = path.startsWith("/") ? path : `/${path}`;
      // Avoid prepending language prefix multiple times or if it already has one
      if (cleanPath.startsWith("/pt/") || cleanPath.startsWith("/en/") || cleanPath.startsWith("/gn/") || cleanPath === "/pt" || cleanPath === "/en" || cleanPath === "/gn") {
        return cleanPath;
      }
      const result = `${prefix}${cleanPath === "/" ? "" : cleanPath}`;
      return result || "/";
    }
  };
}
